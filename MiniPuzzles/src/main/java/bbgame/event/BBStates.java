/**
 * BBGame states.
 */
package bbgame.event;

/**
 * This class contains BBGame states.
 */
public class BBStates {

    /**
     * States.
     */
    public enum State {

        /**
         * Run the game.
         */
        RUN,
        /**
         * Return to the main menu.
         */
        RETURN_TO_MENU,
        /**
         * The game is over.
         */
        GAME_OVER
    }
}
